    private void connectSocketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_connectSocketActionPerformed
        // TODO add your handling code here:
        try {
            String server = "127.0.0.1";
            int port = 6666;
            String p = Integer.toString(port);
            mySocket = new Socket(server, port);
            outStream = new DataOutputStream(mySocket.getOutputStream());
            inStream = new BufferedReader(new InputStreamReader(mySocket.getInputStream()));
            outputText.setText("You are connected to the server.");
            serverIP.setText(server); 
            portAddress.setText(p);
        
	} catch (IOException exc) {
	System.out.println("Error is : " + exc.toString());
	}
    }//GEN-LAST:event_connectSocketActionPerformed

    private void disconnectSocketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_disconnectSocketActionPerformed
        // TODO add your handling code here:
        try {
            outStream.close();
            inStream.close();
            mySocket.close();
            commandTextBox.setText(""); 
            serverIP.setText(""); 
            portAddress.setText(""); 
            clientName.setText(""); 
            outputText.setText("Connection has been terminated.");
            System.out.println("Socket closed");
        } catch (IOException exc) {
	System.out.println("Error is : " + exc.toString());
	}
    }//GEN-LAST:event_disconnectSocketActionPerformed

