    private void CommandRunButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CommandRunButtonActionPerformed
        // TODO add your handling code here:
        
        try{
            String sendData = ""; 
            sendData = commandTextBox.getText().trim();
            outStream.writeBytes(sendData + "\n");
            String response = inStream.readLine();
            
            if(response.equals("Welcome to our file system, you have been added as a new client."))
            {
                clientName.setText(sendData.split("[ ;]")[1]);
                outputText.setText(response);
            }

            else
            {
                outputText.setText(response);
            }
          
        }catch (IOException e) {

        }
       
    }//GEN-LAST:event_CommandRunButtonActionPerformed

